/*
Switch-Case
   어떤 값이 switch 안으로 주어지고
   주어진 값 중에서 일치하는 값이 case 에 있다면
   case 에서 어떤 특정 행동을 진행할 수 있도록 설정
   그런데 주어진 값이 일치하는 case가 없다면 default 로 이동하게됨
   
   case는 break가 없으면 나머지 밑에 있는 case와 default에 작성된
   코드들이 모두 실행이 되기 때문에
   case 에다가 필요한 기능을 작성하고 break를 작성해주는 것이 좋음
   
   default 는 필수로 작성하는 것은 아님
   
  switch(변수 또는 표현식) {
  	case 값1 :
  		변수가 값1에 해당하는 내용일 떄
  		실행될 동작 작성
  		break; //break 또한 필수가 아님!
	case 값1 :  case 값3:
		변수가 값2와 값3 중에 둘종 하나일때 
		실행될 동작 작성
		break;
	default :
		모든 case에 해당하는  내용이 없을 때 동작하는 공간
		break;
  
  }
 
 
 * */
